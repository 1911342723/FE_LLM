def all_chars() -> list[str]:
    """语料里出现的全部字符（构建字表用）。"""
    s = set()
    for p, r in load_dialogues():
        s.update(p)
        s.update(r)
    return sorted(s)


# —— 从教师生成的真实数据集加载（落盘的，省钱不重生成）——
import json as _json
import os as _os

_DIALOGUE_PATH = _os.path.join("data", "dialogue", "dialogues.jsonl")


def load_dialogues() -> list[tuple[str, str]]:
    """优先加载教师生成的大语料；不存在则回退内置 37 条玩具对话。"""
    if _os.path.exists(_DIALOGUE_PATH):
        out = []
        with open(_DIALOGUE_PATH, "r", encoding="utf-8") as f:
            for line in f:
                try:
                    o = _json.loads(line)
                    p, r = o["prompt"].strip(), o["response"].strip()
                    if p and r:
                        out.append((p, r))
                except (_json.JSONDecodeError, KeyError):
                    continue
        if out:
            return out
    return DIALOGUES


